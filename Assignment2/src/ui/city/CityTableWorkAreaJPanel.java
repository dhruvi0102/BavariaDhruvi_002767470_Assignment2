private void populateCombo() {
        cmbCity.removeAllItems();
        for (City s : cityDirectory.getCities()) {
            cmbCity.addItem(s);
        }
    }
private void populateCityTable() {
        City selectedCity = (City) cmbCity.getSelectedItem();
        if (selectedCity == null || selectedCity.getCityData() == null) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCommunityList.getModel();
        model.setRowCount(0);

        for (Community community : selectedCity.getCityData().getCommunities()) {
            Object row[] = new Object[4];
            row[0] = selectedCity.getCityName();
            row[1] = community.getName();
            model.addRow(row);
        }
    }
